#include <stdio.h>
#include <string.h>

void echoArgs(char **argv[]);

int main(int argc, char *argv[]){
  if (argc == 2) {
    echoArgs(*argv[]);
  }

}

void echoArgs(char **argv[]){
  if(argv[1][1] == '-'){
    switch(argv){
      case 'h':printf("this program print's what you type\n");
      default: printf("expected input.");
    }
  }
}
